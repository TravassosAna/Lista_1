dia = int(input("Insira um número de 1 a 7 para achar o dia da semana:"))
if dia == 1:
  print("Domingo")
elif dia == 2:
  print("Segunda")
elif dia == 3:
  print("terça")
elif dia == 4:
  print("quarta")
elif dia == 5:
  print("quinta")
elif dia == 6:
  print("sexta")
elif dia == 7:
  print("sábado")
else:
  print("erro")
